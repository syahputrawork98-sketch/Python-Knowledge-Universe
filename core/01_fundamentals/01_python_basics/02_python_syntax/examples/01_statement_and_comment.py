# Di bawah ini adalah statement terpisah baru:
tujuan = "Bintang Alpha Centauri"

# Meskipun tidak ada batas seperti `;` dalam bahasa lain, 
# python tahu mereka berbeda instruksi karena dibaca perbaris dari kiri.
print(nama_pengguna, "siap meluncur ke", tujuan)
